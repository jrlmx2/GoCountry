package country

import (
	"fmt"
	"strings"
)

type Country struct {
	full       string
	code_two   string
	code_three string
	num        string
}

func (c *Country) Full() string {
	return c.full
}

func (c *Country) CodeTwo() string {
	return c.code_two
}

func (c *Country) CodeThree() string {
	return c.code_three
}

func (c *Country) Number() string {
	return c.num
}

func (c *Country) ExistsFull(text string) bool {
	if strings.Contains(text, strings.ToLower(c.full)) {
		return true
	} else {
		return false
	}
}

func (c *Country) ExistsTwo(text string) bool {
	if strings.Contains(text, c.code_two) {
		return true
	} else {
		return false
	}
}

func (c *Country) ExistsThree(text string) bool {
	if strings.Contains(text, c.code_three) {
		return true
	} else {
		return false
	}
}

func (c *Country) ExistsNum(text string) bool {
	if strings.Contains(text, c.num) {
		return true
	} else {
		return false
	}
}

type Options struct {
	full       bool
	two_code   bool
	three_code bool
	num        bool
}

func FindByNum(num int) *Country {
	search := fmt.Sprintf("%03d", num)
	for _, c := range countries {
		if c.ExistsNum(search) {
			return c
		}
	}
	return nil
}

func Contains(text string, options *Options) []*Country {
	text = strings.ToLower(text)
	contains := make([]*Country, 0)

	for _, c := range countries {
		if options.full && c.ExistsFull(text) || options.two_code && c.ExistsTwo(text) || options.three_code && c.ExistsThree(text) || options.num && c.ExistsNum(text) {
			contains = append(contains, c)
		}
	}

	return contains
}

